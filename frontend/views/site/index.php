<h1>Herzlich Willkommen</h1>

<p>
Ursprünglich unter dem Namen
<b>Goin' up</b>
gegründet gibt es uns seit dem 17.01.2002, allerdings in wechselnder Besetzung und mit wechselnden Chorleitern.
<br>
Seit August 2009 heißen wir
<b>We for G</b>
.
</p>
<p>We for G ist für uns nicht nur ein Name, sondern unser Programm. So steht das "G" nicht nur für God oder Glory, sondern auch für Gemeinschaft, Güte, Gnade und Gesang.</p>
<p>
Gegründet wurde We for G bzw. Goin' up von Herrn Kremer, der diesen bis März 2006 geleitet hat. Danach hat Mareile Osteneck die Leitung übernommen, allerdings nur für ein paar Monate und so kam der nächste Wechsel, diesmal Anke Seydell.
<br>
Leider endet auch dieses Engagement bereits nach gut einem Jahr und es ging weiter mit Nathalie Doan, gefolgt von Albrecht Noetzel, der den Chor 4,5 Jahre leitete. Nachdem Albrecht Noetzel uns aus persönlichen Gründen verließ übernahm Silvia Becker kurzzeitig den Chor. Aber auch Silvia Becker verließ uns nach zwei Jahren, woraufhin René Sass seit 2016 die Chorleitung übernahm.
</p>
<p class="paragraph-end">Da wir ein Gospelchor sind beschränkt sich unser Repertoire vorrangig auf Gospelsongs. Aber natürlich lassen wir uns den Spaß anderer Genres wie Rock und Pop nicht entgehen.</p>

<h2>Du möchtest mitsingen?</h2>
